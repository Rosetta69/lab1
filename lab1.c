#include <stdio.h>

int main() {
    float x = 0; // ввід значення х
    printf("Введіть значення x: ");
    scanf("%f", &x);
    
    if (x >= -2 && x <= 3) { // перевірка умови належності до відрізку
        printf("%f належить відрізку [-2,3]", x);
    } else {
        printf("%f не належить відрізку [-2,3]", x);
    }
    return 0;
}
 